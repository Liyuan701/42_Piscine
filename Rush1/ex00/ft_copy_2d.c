/*copie un tableau en 2d*/
void	ft_copy_2d(int dest[6][6], int src[6][6])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i <= 5)
	{
		j = 1;
		while (j <= 4)
		{
			dest[i][j] = src[i][j];
			j++;
		}
		i++;
	}
}
